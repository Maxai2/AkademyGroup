string * Teacher::getSkill() { return this->skills; }
//---------------------------------------------------------------------------------------------------
void Teacher::setSkill(string arr[])
{
	for (int i = 0; arr[i].size() == 0; i++)
		this->skills[i] = arr[i];
}
